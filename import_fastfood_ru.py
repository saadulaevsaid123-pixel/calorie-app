def main():
    print(f"Всего: {len(FOODS)} продуктов")
    conn = psycopg2.connect(DATABASE_URL)
    cur = conn.cursor()

    # Удаляем старые записи McDonald's и KFC
    cur.execute("DELETE FROM custom_foods WHERE name LIKE 'McDonald%' OR name LIKE 'KFC%'")
    deleted = cur.rowcount
    print(f"Удалено старых записей: {deleted}")
    conn.commit()

    cur.execute("SELECT LOWER(name) FROM custom_foods")
    existing = set(r[0] for r in cur.fetchall())

    added = 0
    for name, cal, protein, fat, carbs, category in FOODS:
        if name.lower() in existing:
            continue
        cur.execute("""INSERT INTO custom_foods (name,cal,protein,fat,carbs,category,added_by,created_at)
            VALUES (%s,%s,%s,%s,%s,%s,'fastfood_ru','2026-01-01')""",
            (name, cal, protein, fat, carbs, category))
        existing.add(name.lower())
        added += 1

    conn.commit()
    cur.execute("SELECT COUNT(*) FROM custom_foods")
    print(f"✅ Добавлено: {added}")
    print(f"📦 Всего в базе: {cur.fetchone()[0]:,}")
    cur.close()
    conn.close()

if __name__ == "__main__":
    main()
